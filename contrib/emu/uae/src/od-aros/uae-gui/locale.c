static int OpenCatalog_UAE(void)        
{
    ULONG count;

    if (LocaleBase != NULL)
    {
        catalog = OpenCatalog
	(
	    NULL, "UAE.catalog",
	    OC_Version, 0,
	    TAG_DONE
	);
    }

    for (count = 0; count < sizeof(msg_ARRAY)/sizeof(msg_ARRAY[0]); count++)
    {
	*msg_ARRAY[count].msg_STR = LocaleBase != NULL
	    ? GetCatalogStr
	      (
	          catalog, msg_ARRAY[count].msg_ID, msg_ARRAY[count].msg_DEFSTR
	      )
	    : (CONST_STRPTR)msg_ARRAY[count].msg_DEFSTR;
    }

    return 1;
}

static void CloseCatalog_UAE(void)
{
    if (LocaleBase != NULL)
        CloseCatalog(catalog);
}

ADD2INIT(OpenCatalog_UAE, 0);
ADD2EXIT(CloseCatalog_UAE, 0);
