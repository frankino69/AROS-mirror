/* ------------- Bstrcpy ------------- */
char*
Bstrcpy(char *dest, const char *src)
{
	register char	*d=dest;
	register const char	*s=src;
	do {} while ((*d++=*s++) != 0);
	return dest;
} /* Bstrcpy */

/* ------------- Bstrlen ------------- */
unsigned
Bstrlen(const char *s)
{
	register unsigned	len=0;
	register const char	*str=s;
	while (*str++) len++;
	return len;
} /* Bstrlen */

/* ------------- Bstrcmp ------------- */
int
Bstrcmp(const char *s1, const char *s2)
{
	register const char	*a=s1;
	register const char	*b=s2;

	for (; *a && *b; a++, b++) {
		if (*a<*b)
			return -1;
		else
		if (*a>*b)
			return  1;
	}
	if (*a) return  1;
	if (*b) return -1;
	return 0;
} /* Bstrcmp */

/* ------------- Bstrcat ------------- */
char*
Bstrcat(char *dest, const char *src)
{
	register char	*d=dest;
	register const char	*s=src;
	do {} while (*d++ != 0);
	d--;
	do {} while ((*d++=*s++) != 0);
	return dest;
} /* Bstrcat */

/* ------------- Bstrchr ------------- */
const char*
Bstrchr(const char *s, int c)
{
	register const char *str=s;
	for ( ;*str; str++)
		if (*str==(char)c)
			return str;
	return (char *)0;
} /* Bstrchr */

